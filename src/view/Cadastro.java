    private void cadastroButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastroButtonActionPerformed
        // TODO add your handling code here:
        
        String nome = nomeCadastroTxt.getText().trim();
        String usuario = usuarioCadastroTxt.getText().trim();
        String senha = senhaCadastroTxt.getText().trim();

        // ValidaÃ§Ã£o dos campos
        if (nome.isEmpty()) {
            JOptionPane.showMessageDialog(null, "O campo Nome nÃ£o pode estar vazio!", "Erro", JOptionPane.ERROR_MESSAGE);
                nomeCadastroTxt.requestFocus(); 
                    return; 
        }

        if (usuario.isEmpty()) {
            JOptionPane.showMessageDialog(null, "O campo UsuÃ¡rio nÃ£o pode estar vazio!", "Erro", JOptionPane.ERROR_MESSAGE);
                usuarioCadastroTxt.requestFocus();
                    return;
        }

        if (senha.isEmpty()) {
            JOptionPane.showMessageDialog(null, "O campo Senha nÃ£o pode estar vazio!", "Erro", JOptionPane.ERROR_MESSAGE);
                senhaCadastroTxt.requestFocus();
                return;
        }

        
        Usuario usu = new Usuario();
        usu.setNome(nome);
        usu.setUsuario(usuario);
        usu.setSenha(senha);


        controllerUsuario controllerUsuario = new controllerUsuario();
        controllerUsuario.cadastrarUsuario(usu); // MÃ©todo deve existir no UsuarioDAO

// Limpa os campos apÃ³s o cadastro
        nomeCadastroTxt.setText("");
        usuarioCadastroTxt.setText("");
        senhaCadastroTxt.setText("");

// Exibe mensagem de sucesso
JOptionPane.showMessageDialog(null, "UsuÃ¡rio cadastrado com sucesso!");
                                                      
    }//GEN-LAST:event_cadastroButtonActionPerformed

